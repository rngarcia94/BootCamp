public class Fraccion {

    public static float sumar(Number a, Number b){
        return a.floatValue() + b.floatValue();
    }

    public static float restar(Number a, Number b){
        return a.floatValue() - b.floatValue();
    }

    public static float multiplicar(Number a, Number b){
        return  a.floatValue() * b.floatValue();
    }

    public  static float dividir(Number a, Number b){
        return a.floatValue() / b.floatValue();
    }
}
